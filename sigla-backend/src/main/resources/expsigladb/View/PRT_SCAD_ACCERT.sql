FROM ACCERTAMENTO_SCADENZARIO, ACCERTAMENTO, TERZO, UNITA_ORGANIZZATIVA a, UNITA_ORGANIZZATIVA b
WHERE ACCERTAMENTO_SCADENZARIO.ESERCIZIO_ORIGINALE=ACCERTAMENTO.ESERCIZIO_ORIGINALE AND
          ACCERTAMENTO_SCADENZARIO.PG_ACCERTAMENTO=ACCERTAMENTO.PG_ACCERTAMENTO AND
