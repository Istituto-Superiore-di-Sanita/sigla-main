	  (ACCERTAMENTO.CD_TIPO_DOCUMENTO_CONT='ACR' OR ACCERTAMENTO.CD_TIPO_DOCUMENTO_CONT='ACR_RES')
ORDER BY ESERCIZIO,ACCERTAMENTO_SCADENZARIO.DT_SCADENZA_INCASSO, ACCERTAMENTO_SCADENZARIO.ESERCIZIO_ORIGINALE, ACCERTAMENTO_SCADENZARIO.PG_ACCERTAMENTO,ACCERTAMENTO_SCADENZARIO.PG_ACCERTAMENTO_SCADENZARIO;

