from INCARICHI_RICHIESTA, UNITA_ORGANIZZATIVA CDS, UNITA_ORGANIZZATIVA UO
Where INCARICHI_RICHIESTA.CD_CDS = CDS.CD_UNITA_ORGANIZZATIVA
And INCARICHI_RICHIESTA.CD_UNITA_ORGANIZZATIVA = UO.CD_UNITA_ORGANIZZATIVA
And stato In ('C','D')
)
Order By 1,2
;
